def __getattr__(name: str):
    if name in {
        "META_STRATEGY_CHARACTERIZATION_SCHEMA_VERSION",
        "REQUIRED_CHARACTERIZATION_FIXTURES",
        "build_current_behavior_characterization",
    }:
        from backend.app.algorithms.meta_strategy import characterization

        return getattr(characterization, name)
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")


